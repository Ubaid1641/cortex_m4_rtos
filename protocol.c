/**
 * @brief Calculate CRC-8 over data buffer
 * 
 * Uses precomputed lookup table for efficiency.
 * CRC covers Type, Length, and Payload fields.
 */
uint8_t crc8_calc(const uint8_t* data, uint8_t len) {
    uint8_t crc = 0x00;
    uint8_t i;
    
    for (i = 0; i < len; i++) {
        crc = crc8_table[crc ^ data[i]];
    }
    
    return crc;
}

/**
 * @brief Encode a uint32_t sensor value into a binary packet
 * 
 * Packet format: [Header:0xAA][Type:1Byte][Len:1Byte][Payload:N][CRC8]
 * The CRC is calculated over Type + Length + Payload.
 * 
 * @param buffer Output buffer (must be at least PROTOCOL_MIN_PACKET_SIZE + payload_size bytes)
 * @param type Protocol message type
 * @param sensor_value The 32-bit sensor value to encode (4 bytes, little-endian)
 * @return Total packet length including header and CRC
 */
uint8_t protocol_encode(uint8_t* buffer, protocol_type_t type, uint32_t sensor_value) {
    uint8_t payload_len = 4; /* 4 bytes for uint32_t */
    uint8_t crc_data_len;
    uint8_t crc;
    
    /* Header */
    buffer[0] = PROTOCOL_HEADER;
    
    /* Type */
    buffer[1] = (uint8_t)type;
    
    /* Length (payload length only) */
    buffer[2] = payload_len;
    
    /* Payload: encode uint32_t as 4 bytes (little-endian) */
    buffer[3] = (uint8_t)(sensor_value & 0xFF);
    buffer[4] = (uint8_t)((sensor_value >> 8) & 0xFF);
    buffer[5] = (uint8_t)((sensor_value >> 16) & 0xFF);
    buffer[6] = (uint8_t)((sensor_value >> 24) & 0xFF);
    
    /* Calculate CRC over Type + Length + Payload */
    crc_data_len = 1 + 1 + payload_len; /* Type(1) + Len(1) + Payload(4) */
    crc = crc8_calc(&buffer[1], crc_data_len);
    
    /* Append CRC */
    buffer[3 + payload_len] = crc;
    
    /* Return total packet size: Header + Type + Len + Payload + CRC */
    return 1 + 1 + 1 + payload_len + 1;
}
